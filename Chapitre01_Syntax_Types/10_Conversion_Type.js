//conversion string en numbre
let myString = '100'
console.log(myString + 1); //donne 1001
let myStringConversion = Number(myString);
console.log(typeof myStringConversion);
console.log(myStringConversion + 2); //donne 102

//conversion number en string
let myNumber = 60;
console.log(myNumber + 1); //donne 61
let myNumberConversion = String(myNumber);
console.log(typeof myNumberConversion);
console.log(myNumberConversion + 1); //donne 601